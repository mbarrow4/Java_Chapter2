//This program demonstrates the different escape characters

public class Escape_Characters //Escape Character Class
{

	public static void main(String[] args) //Beginning of the Main Method
	{
		//New Line Escape Character
		System.out.println("New \nLine");
		System.out.println("This \nis \na \nLine");
		
		//Tab Escape Character
		System.out.println("\t Tab Character");
		
		//Back Slash Character
		System.out.println("\\");
		
		//Double Quotes
		System.out.println("\"");
	

	}//End of Main Method

}//End of Escape Character Class
